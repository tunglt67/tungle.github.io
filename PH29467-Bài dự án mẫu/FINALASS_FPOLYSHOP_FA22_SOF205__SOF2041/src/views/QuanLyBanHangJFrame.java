    private void addGH(int index) {
        boolean check = true;
        // check xem giỏ hàng có SP chưa
        String id = listSP.get(index).getId();
        listGH = iQuanLyHoaDonChiTietService.getHDCTByMaHD(txtMaHD.getText());
        for (HoaDonChiTiet hdct : listGH) {
            if (hdct.SPV().getId().equalsIgnoreCase(id)) {
                int SL = hdct.getSL() + 1;
                hdct.setSL(SL);
                double donGia = SL * hdct.SPV().getGiaBan();
                hdct.setDonGia(donGia);
                iQuanLyHoaDonChiTietService.updateSL(hdct);
                check = false;
            }
        }
        if (check == true) {
            HoaDonChiTiet hdct = new HoaDonChiTiet(iQuanLyHoaDonService.getHDByMa(txtMaHD.getText()), iQuanLyChiTietSanPhamService.getCTSPByid(id), 1, listSP.get(index).getGiaBan());
            iQuanLyHoaDonChiTietService.addSP(hdct);
        }
        //add row tblGioHang
        loadDataToTableGH();
        //Tinh Tien
        txtTongTien.setText(String.valueOf(getThanhTien()));
    }

    public double getThanhTien() {
        double money = 0;
        for (int i = 0; i < tblListGH.getRowCount(); i++) {
            money += Double.parseDouble(tblListGH.getValueAt(i, 5) + "");
        }
        return money;
    }

    public void updateSL(SanPhamViewModel ctsp, int soLuong) {
        iQuanLyChiTietSanPhamService.updateSL(ctsp, soLuong);
        for (int i = 0; i < listSP.size() - 1; i++) {
            if (listSP.get(i).getId().endsWith(ctsp.getId())) {
                tblListSP.setValueAt(ctsp.getSlTon() - soLuong, i, 5);
            }
        }
    }

    public void showHD(HoaDon h) {
        txtMaHD.setText(h.getMa());
        txtNgayTao.setText(XDate.toString(h.getNgayTao()));
        txtTienThua.setText(String.valueOf(getThanhTien()));
        txtTienKhachDua.setText("");
        txtTienThua.setText("");
        loadDataToTableGH();
    }

    public HoaDon getHoaDon() {
        int dem = Integer.parseInt(listViewHD.get(0).getMaHD().substring(2, listViewHD.get(0).getMaHD().length())) + 1;
        String ma = "HĐ" + dem;
        return new HoaDon(ma, XDate.now(), 1);
    }

    public void thanhToan() {
        HoaDon h = iQuanLyHoaDonService.getHDByMa(txtMaHD.getText());
        iQuanLyHoaDonService.thanhToan(h);
    }

    public void search(String keyWord) {
        listSP = iQuanLyChiTietSanPhamService.search(keyWord);
        dtm.setRowCount(0);
        for (ChiTietSanPhamViewModel ctsp : listSP) {
            dtm.addRow(ctsp.toDataRow());
        }
    }

