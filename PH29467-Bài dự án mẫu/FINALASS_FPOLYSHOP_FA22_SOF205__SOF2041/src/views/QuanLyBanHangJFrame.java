    private void loadDataToTable(List<ChiTietSanPhamViewModel> list) {
        dtm.setRowCount(0);
        for (ChiTietSanPhamViewModel ctspvm : list) {
            dtm.addRow(ctspvm.toDataRow());
        }
    }

    private void loadDataToTableGH() {
        listGH = iQuanLyHoaDonChiTietService.getHDCTByMaHD(txtMaHD.getText());
        listGHView = iQuanLyHoaDonChiTietService.getHDCTView(txtMaHD.getText());
        dtmGH.setRowCount(0);
        for (GioHangViewModel ghvm : listGHView) {
            dtmGH.addRow(ghvm.toDataRow());
        }
    }

    private void loadDataToTableHD(List<HoaDonViewModel> list) {
        listViewHD = iQuanLyHoaDonService.getViewHoaDon();
        dtmHD.setRowCount(0);
        for (HoaDonViewModel hdvm : list) {
            dtmHD.addRow(hdvm.toDataRow());
        }
    }

    private void loadDataToTableHDTT() {
        int tt = 0;
        if (rdoCThanhToan.isSelected() == true) {
            tt = 0;
        } else if (rdoDThanhToan.isSelected() == true) {
            tt = 1;
        }
        listViewHD = iQuanLyHoaDonService.getViewHoaDonByTT(tt);
        dtmHD.setRowCount(0);
        for (HoaDonViewModel hdvm : listViewHD) {
            dtmHD.addRow(hdvm.toDataRow());
        }
    }

